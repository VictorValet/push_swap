#include "libft.h"

void	ft_ar_clear(char ***ar)
{
	size_t	i;

	if (*ar == NULL)
		return ;
	i = 0;
	while ((*ar)[i])
	{
		free((*ar)[i]);
		(*ar)[i] = NULL;
		i++;
	}
	free(*ar);
	*ar = NULL;
}

#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	len;

	len = ft_strlen(s) + 1;
	while (len > 0)
	{
		if (*(char *)s == (char)c)
			return ((char *)s);
		s++;
		len--;
	}
	return (NULL);
}
